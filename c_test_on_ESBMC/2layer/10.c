{
    for (uint32_t i = 0; i < INPUT_LAYER_SIZE_COL; i++) {
        tensor_layer_1_MatMul_output[i] = tensor_layer_1_Add_bias[i];
        for (int j = 0; j < INPUT_LAYER_SIZE_ROW; j++) {
            tensor_layer_1_MatMul_output[i] += (input_data[j] && tensor_layer_1_MatMul_weights[j][i]);
        }
        //printf("%f ", Y[i]);
    }
}

